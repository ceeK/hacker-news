import UIKit

extension UIColor {
    public class func appPrimaryColor() -> UIColor {
        return UIColor(red: 255.0/255.0, green: 102.0/255.0, blue: 0.0, alpha: 1.0)
    }
    
    public class func appTitleColor() -> UIColor {
        return UIColor(red: 30.0/255.0, green: 32.0/255.0, blue: 34.0/255.0, alpha: 1.0)
    }

}
